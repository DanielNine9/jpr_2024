package DuAn.entity;

import java.util.Date;

/*
    Thuộc tính: Mã người học, họ tên, ngày sinh, giới tính, điện thoại, email,
ghi chú, mã nhân viên đã thêm người học, ngày đăng ký
    Contrucotor, getter và setter
 */
/**
 *
 * @author dinhh
 */
public class NguoiHoc {

    private int maNH;
    private String hoTen;
    private Date ngaySinh; //XDate.addDayss(new Date(), -365*2);
    private boolean gioiTinh;
    private String dienThoai;
    private String email;
    private String ghiChu;
    private int maNV;
    private Date ngayDK = new Date();
    private String hinh;
    private boolean deleted = false;

    public boolean isDeleted() {
        return deleted;
    }

    public void setDeleted(boolean deleted) {
        this.deleted = deleted;
    }

    @Override
    public String toString() {
        return this.maNH + ", " + this.hoTen; //masv + hoten
    }

    public NguoiHoc() {
    }

    public String getHinh() {
        return hinh;
    }

    public void setHinh(String hinh) {
        this.hinh = hinh;
    }

    public NguoiHoc(String hoTen, Date ngaySinh, boolean gioiTinh, String dienThoai, String email, String ghiChu, int maNV) {
        this.hoTen = hoTen;
        this.ngaySinh = ngaySinh;
        this.gioiTinh = gioiTinh;
        this.dienThoai = dienThoai;
        this.email = email;
        this.ghiChu = ghiChu;
        this.maNV = maNV;
    }

    public int getMaNH() {
        return maNH;
    }

    public void setMaNH(int maNH) {
        this.maNH = maNH;
    }

    public String getHoTen() {
        return hoTen;
    }

    public void setHoTen(String hoTen) {
        this.hoTen = hoTen;
    }

    public Date getNgaySinh() {
        return ngaySinh;
    }

    public void setNgaySinh(Date ngaySinh) {
        this.ngaySinh = ngaySinh;
    }

    public boolean isGioiTinh() {
        return gioiTinh;
    }

    public void setGioiTinh(boolean gioiTinh) {
        this.gioiTinh = gioiTinh;
    }

    public String getDienThoai() {
        return dienThoai;
    }

    public void setDienThoai(String dienThoai) {
        this.dienThoai = dienThoai;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getGhiChu() {
        return ghiChu;
    }

    public void setGhiChu(String ghiChu) {
        this.ghiChu = ghiChu;
    }

    public int getMaNV() {
        return maNV;
    }

    public void setMaNV(int maNV) {
        this.maNV = maNV;
    }

    public Date getNgayDK() {
        return ngayDK;
    }

    public void setNgayDK(Date ngayDK) {
        this.ngayDK = ngayDK;
    }

}
